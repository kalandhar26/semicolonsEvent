@app.get("/predict", response_model=list[PredictionResult], tags=["Prediction"])
async def predict_all(explain: bool = False):
    """Return the latest anomaly score for every monitored service."""
    results = []
    for name, state in SERVICES.items():
        llm = None
        if explain and state.last_score >= ANOMALY_THRESHOLD:
            llm = await llm_explain(state.last_raw, state.last_score)

        results.append(PredictionResult(
            service       = name,
            anomaly_score = round(state.last_score, 4),
            is_anomaly    = state.last_score >= ANOMALY_THRESHOLD,
            threshold     = ANOMALY_THRESHOLD,
            features      = state.last_raw,
            trained       = state.trained,
            timestamp     = state.last_seen or datetime.now(timezone.utc).isoformat(),
            recommendation= recommendation(state.last_score, state.last_raw),
            llm_analysis  = llm,
        ))
    return results


@app.post("/predict/{service_name}", response_model=PredictionResult, tags=["Prediction"])
async def predict_one(
    service_name: str,
    background_tasks: BackgroundTasks,
    explain: bool = False,
):
    """On-demand prediction for a specific service — fetches fresh metrics."""
    if service_name not in SERVICES:
        raise HTTPException(status_code=404, detail=f"Service '{service_name}' not monitored.")

    state = SERVICES[service_name]
    async with httpx.AsyncClient() as client:
        raw = await fetch_metrics_for_service(client, service_name)
    if raw is None:
        raise HTTPException(status_code=503, detail="Could not fetch metrics from Prometheus.")

    features = raw_to_features(raw)
    state.add_sample(features)
    if not state.trained and len(state.buffer) >= 20:
        state.train()

    score = state.predict(features)
    state.last_score = score
    state.last_raw   = raw

    llm = None
    if explain and score >= ANOMALY_THRESHOLD:
        llm = await llm_explain(raw, score)

    return PredictionResult(
        service       = service_name,
        anomaly_score = round(score, 4),
        is_anomaly    = score >= ANOMALY_THRESHOLD,
        threshold     = ANOMALY_THRESHOLD,
        features      = raw,
        trained       = state.trained,
        timestamp     = datetime.now(timezone.utc).isoformat(),
        recommendation= recommendation(score, raw),
        llm_analysis  = llm,
    )


@app.post("/train/{service_name}", response_model=TrainResponse, tags=["Model"])
async def retrain(service_name: str):
    """Manually trigger a model retrain on the current rolling buffer."""
    if service_name not in SERVICES:
        raise HTTPException(status_code=404, detail=f"Service '{service_name}' not monitored.")
    state = SERVICES[service_name]
    ok = state.train()
    return TrainResponse(
        service = service_name,
        success = ok,
        samples = len(state.buffer),
        message = f"Trained on {len(state.buffer)} samples." if ok else "Not enough data (need ≥ 20 samples).",
    )


@app.get("/history", tags=["Prediction"])
async def get_history(limit: int = 50, service: Optional[str] = None):
    """Return recent prediction history, newest first."""
    items = list(history)
    if service:
        items = [e for e in items if e["service"] == service]
    return items[:limit]


@app.get("/explain/{service_name}", tags=["LLM"])
async def explain(service_name: str):
    """Ask Ollama Llama 3 to explain the current anomaly (requires Ollama running)."""
    if service_name not in SERVICES:
        raise HTTPException(status_code=404, detail=f"Service '{service_name}' not monitored.")
    state = SERVICES[service_name]
    analysis = await llm_explain(state.last_raw, state.last_score)
    if analysis is None:
        raise HTTPException(
            status_code=503,
            detail="Ollama unavailable. Start it with: docker-compose up ollama && make ollama-pull"
        )
    return {"service": service_name, "analysis": analysis, "anomaly_score": state.last_score}


@app.get("/metrics", tags=["System"], include_in_schema=False)
async def metrics():
    """Prometheus scrape endpoint."""
    return Response(generate_latest(), media_type=CONTENT_TYPE_LATEST)
