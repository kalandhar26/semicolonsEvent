# ── Prometheus helpers ────────────────────────────────────────────────────────
async def query_prometheus(client: httpx.AsyncClient, promql: str) -> Optional[float]:
    """Run an instant PromQL query and return the scalar value."""
    try:
        resp = await client.get(
            f"{PROMETHEUS_URL}/api/v1/query",
            params={"query": promql},
            timeout=5.0,
        )
        data = resp.json()
        results = data.get("data", {}).get("result", [])
        if results:
            return float(results[0]["value"][1])
        return None
    except Exception as exc:
        log.warning("Prometheus query failed [%s]: %s", promql, exc)
        return None


async def fetch_metrics_for_service(
    client: httpx.AsyncClient,
    service: str,
) -> Optional[dict]:
    """
    Pull the features we feed into Isolation Forest.

    Features (all from Spring Boot Actuator via Prometheus):
      1. error_rate_pct   — % of requests returning 5xx in the last 1 min
      2. latency_p95_ms   — 95th-percentile response time in ms
      3. latency_p50_ms   — 50th-percentile response time
      4. req_rate_per_sec — requests per second (volume signal)
      5. jvm_heap_used_mb — JVM heap usage (memory pressure signal)
      6. gc_pause_rate    — GC pause events per second
    """
    instance = f"{service}:8080" if "service" in service else service

    # 5xx error rate
    error_rate = await query_prometheus(
        client,
        f'rate(http_server_requests_seconds_count{{instance="{instance}",status=~"5.."}}[1m])'
        f' / rate(http_server_requests_seconds_count{{instance="{instance}"}}[1m]) * 100'
    )

    # p95 latency
    latency_p95 = await query_prometheus(
        client,
        f'histogram_quantile(0.95, rate(http_server_requests_seconds_bucket{{instance="{instance}"}}[1m])) * 1000'
    )

    # p50 latency
    latency_p50 = await query_prometheus(
        client,
        f'histogram_quantile(0.50, rate(http_server_requests_seconds_bucket{{instance="{instance}"}}[1m])) * 1000'
    )

    # Request rate
    req_rate = await query_prometheus(
        client,
        f'rate(http_server_requests_seconds_count{{instance="{instance}"}}[1m])'
    )

    # JVM heap
    heap_mb = await query_prometheus(
        client,
        f'jvm_memory_used_bytes{{instance="{instance}",area="heap"}} / 1048576'
    )

    # GC pause rate
    gc_rate = await query_prometheus(
        client,
        f'rate(jvm_gc_pause_seconds_count{{instance="{instance}"}}[1m])'
    )

    # If Prometheus has no data yet (service just started), use fallback from Actuator
    if error_rate is None:
        try:
            actuator_resp = await client.get(
                f"{ORDER_SERVICE_URL}/actuator/metrics/http.server.requests",
                timeout=3.0,
            )
            # Rough extraction — replace with proper parsing in production
            error_rate = 2.0
        except Exception:
            error_rate = 0.0

    raw = {
        "error_rate_pct":    round(error_rate  or 0.0, 3),
        "latency_p95_ms":    round(latency_p95 or 120.0, 2),
        "latency_p50_ms":    round(latency_p50 or 80.0, 2),
        "req_rate_per_sec":  round(req_rate    or 0.0, 3),
        "jvm_heap_used_mb":  round(heap_mb     or 0.0, 1),
        "gc_pause_rate":     round(gc_rate     or 0.0, 4),
    }
    return raw


def raw_to_features(raw: dict) -> list[float]:
    return [
        raw["error_rate_pct"],
        raw["latency_p95_ms"],
        raw["latency_p50_ms"],
        raw["req_rate_per_sec"],
        raw["jvm_heap_used_mb"],
        raw["gc_pause_rate"],
    ]


def recommendation(score: float, raw: dict) -> str:
    if score < 0.45:
        return "Service healthy — no action required."
    if score < 0.75:
        return (
            f"Elevated anomaly score ({score:.2f}). "
            f"Error rate {raw['error_rate_pct']:.1f}%, latency p95 {raw['latency_p95_ms']:.0f}ms. "
            "Continue monitoring."
        )
    return (
        f"CRITICAL: Anomaly score {score:.2f} exceeds threshold {ANOMALY_THRESHOLD}. "
        f"Error rate {raw['error_rate_pct']:.1f}%, latency p95 {raw['latency_p95_ms']:.0f}ms. "
        "Triggering circuit breaker."
    )


async def llm_explain(raw: dict, score: float) -> Optional[str]:
    """Ask Ollama Llama 3 to explain the anomaly in plain English."""
    prompt = (
        "You are an SRE assistant. Analyze these microservice metrics and give a "
        "one-sentence root cause hypothesis in plain English (no markdown, no bullet points):\n\n"
        f"Error rate: {raw['error_rate_pct']:.1f}%\n"
        f"Latency p95: {raw['latency_p95_ms']:.0f}ms\n"
        f"Latency p50: {raw['latency_p50_ms']:.0f}ms\n"
        f"Request rate: {raw['req_rate_per_sec']:.2f} req/s\n"
        f"JVM heap: {raw['jvm_heap_used_mb']:.0f} MB\n"
        f"Anomaly score: {score:.2f}/1.00\n"
    )
    try:
        async with httpx.AsyncClient(timeout=15.0) as client:
            resp = await client.post(
                f"{OLLAMA_URL}/api/generate",
                json={"model": OLLAMA_MODEL, "prompt": prompt, "stream": False},
            )
            return resp.json().get("response", "").strip()
    except Exception as exc:
        log.debug("Ollama unavailable: %s", exc)
        return None


# ── Background polling loop ───────────────────────────────────────────────────
async def poll_loop():
    """Continuously polls Prometheus, updates models, and publishes anomaly scores."""
    log.info("Poll loop started — interval %ds, threshold %.2f", POLL_INTERVAL, ANOMALY_THRESHOLD)

    # Warm-up: collect baseline data for TRAINING_WINDOW / 5 polls before first train
    warmup_target = max(20, TRAINING_WINDOW // 5)
    samples_collected = {s: 0 for s in SERVICES}

    async with httpx.AsyncClient() as client:
        while True:
            start = time.perf_counter()

            for service_name, state in SERVICES.items():
                try:
                    raw = await fetch_metrics_for_service(client, service_name)
                    if raw is None:
                        continue

                    features = raw_to_features(raw)
                    state.add_sample(features)
                    state.last_raw  = raw
                    state.last_seen = datetime.now(timezone.utc).isoformat()
                    samples_collected[service_name] += 1

                    # Auto-train once we have enough warm-up data
                    if not state.trained and samples_collected[service_name] >= warmup_target:
                        log.info("[%s] Warm-up complete — training initial model", service_name)
                        state.train()

                    # Periodic retrain every TRAINING_WINDOW samples
                    if state.trained and samples_collected[service_name] % TRAINING_WINDOW == 0:
                        state.train()

                    score = state.predict(features)
                    state.last_score = score
                    prom_anomaly_score.labels(service=service_name).set(score)

                    event = {
                        "service":       service_name,
                        "anomaly_score": score,
                        "is_anomaly":    score >= ANOMALY_THRESHOLD,
                        "features":      raw,
                        "timestamp":     state.last_seen,
                    }
                    history.appendleft(event)

                    if score >= ANOMALY_THRESHOLD:
                        prom_anomaly_total.labels(service=service_name).inc()
                        log.warning(
                            "[%s] ANOMALY DETECTED score=%.3f error_rate=%.1f%% latency_p95=%.0fms",
                            service_name, score,
                            raw["error_rate_pct"], raw["latency_p95_ms"],
                        )
                        # Notify Healing Service
                        asyncio.create_task(
                            notify_healing_service(service_name, score, raw)
                        )

                except Exception as exc:
                    log.error("[%s] Poll error: %s", service_name, exc)

            elapsed = time.perf_counter() - start
            prom_prediction_latency.observe(elapsed)
            await asyncio.sleep(max(0, POLL_INTERVAL - elapsed))


async def notify_healing_service(service: str, score: float, raw: dict):
    """POST anomaly alert to the Healing Service so it can trigger circuit breaker."""
    try:
        async with httpx.AsyncClient(timeout=5.0) as client:
            await client.post(
                f"{HEALING_SERVICE_URL}/anomaly/alert",
                json={
                    "service":       service,
                    "anomaly_score": score,
                    "error_rate":    raw["error_rate_pct"],
                    "latency_p95":   raw["latency_p95_ms"],
                    "timestamp":     datetime.now(timezone.utc).isoformat(),
                },
            )
        log.info("[%s] Healing Service notified (score=%.2f)", service, score)
    except Exception as exc:
        log.warning("[%s] Could not reach Healing Service: %s", service, exc)


